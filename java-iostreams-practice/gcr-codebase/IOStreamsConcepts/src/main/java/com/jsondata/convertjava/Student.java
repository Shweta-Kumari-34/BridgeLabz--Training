package com.jsondata.convertjava;

public class Student {

	// student name
	private String name;

	// student age
	private int age;

	// constructor
	public Student(String name, int age) {
		this.name = name;
		this.age = age;
	}

	// getters
	public String getName() {
		return name;
	}

	public int getAge() {
		return age;
	}
}
